/* ── Language switcher logic ── */

const LANG_KEY = "elitech_lang";

const getStoredLang = () => {
  try {
    return localStorage.getItem(LANG_KEY) || "fr";
  } catch {
    return "fr";
  }
};

const setStoredLang = (lang) => {
  try {
    localStorage.setItem(LANG_KEY, lang);
  } catch {}
};

let currentLang = getStoredLang();

const applyTranslations = (lang) => {
  currentLang = lang;
  setStoredLang(lang);

  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    const t = TRANSLATIONS[key];
    if (!t) return;

    const text = t[lang] || t.fr;

    if (el.dataset.i18nAttr === "placeholder") {
      el.placeholder = text;
    } else if (el.dataset.i18nAttr === "aria-label") {
      el.setAttribute("aria-label", text);
    } else if (el.classList.contains("est-option")) {
      /* For estimator option labels: keep <input> and emoji <span>, update trailing text */
      const nodes = el.childNodes;
      const lastNode = nodes[nodes.length - 1];
      if (lastNode && lastNode.nodeType === 3) {
        lastNode.textContent = text;
      }
    } else if (text.includes("<strong>") || text.includes("<em>")) {
      el.innerHTML = text;
    } else {
      el.textContent = text;
    }
  });

  /* Update toggle button */
  document.querySelectorAll("[data-lang-toggle]").forEach((btn) => {
    btn.textContent =
      lang === "fr" ? "\uD83C\uDDEC\uD83C\uDDE7" : "\uD83C\uDDEB\uD83C\uDDF7";
    btn.setAttribute(
      "aria-label",
      lang === "fr" ? "Switch to English" : "Passer en français",
    );
  });
};

/* Initialize on DOM ready */
document.addEventListener("DOMContentLoaded", () => {
  applyTranslations(currentLang);

  document.querySelectorAll("[data-lang-toggle]").forEach((btn) => {
    btn.addEventListener("click", (e) => {
      e.preventDefault();
      const newLang = currentLang === "fr" ? "en" : "fr";
      applyTranslations(newLang);
    });
  });
});
